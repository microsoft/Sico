from __future__ import annotations

import json
import logging
from collections.abc import AsyncIterator
from operator import attrgetter
from typing import Any
from collections.abc import Callable

from app.llmhubs import _hub, generate
from app.llmhubs.errors import LLMHubRuntimeError
from app.llmhubs.types import ModelRegistryEntry, Input, InputContent, Request
from app.pb.llmhubs.google.protobuf import Struct
from app.pb.llmhubs.llmhubs import LlmHubRpcBase

logger = logging.getLogger(__name__)

__all__ = ["LLMHubService"]


_INTEGER_REQUEST_OPTION_KEYS = frozenset(
    {
        "max_completion_tokens",
        "max_output_tokens",
        "max_tokens",
        "request_timeout_ms",
        "seed",
        "timeout_ms",
        "top_k",
        "top_logprobs",
    }
)


def _struct_to_dict(struct: Any) -> dict[str, Any]:
    """Convert a betterproto2 Struct (or plain dict) to a Python dict."""
    if struct is None:
        return {}
    if isinstance(struct, dict):
        return struct
    if hasattr(struct, "to_dict"):
        return struct.to_dict()
    if hasattr(struct, "fields"):
        # betterproto2 Struct
        return {k: _value_to_python(v) for k, v in struct.fields.items()}
    return {}


def _normalize_integer_request_options(options: dict[str, Any]) -> dict[str, Any]:
    normalized = dict(options)
    for key in _INTEGER_REQUEST_OPTION_KEYS:
        if key not in normalized:
            continue
        val = normalized[key]
        if not isinstance(val, bool) and isinstance(val, float) and val.is_integer():
            normalized[key] = int(val)
    return normalized


_MISSING_KIND: Any = object()


_VALUE_KIND_EXTRACTORS: dict[str, Callable[[Any], Any]] = {
    "string_value": lambda v: v.string_value,
    "number_value": lambda v: v.number_value,
    "bool_value": lambda v: v.bool_value,
    "struct_value": lambda v: _struct_to_dict(v.struct_value),
    "list_value": lambda v: [_value_to_python(x) for x in v.list_value.values],
    "null_value": lambda _v: None,
}


def _extract_value_from_kind(value: Any, kind: str | None) -> Any:
    """Dispatch on a betterproto2 Value oneof ``kind`` to its Python equivalent."""
    extractor = _VALUE_KIND_EXTRACTORS.get(kind or "")
    if extractor is None:
        return _MISSING_KIND
    return extractor(value)


def _value_to_python(value: Any) -> Any:
    """Convert a betterproto2 Value to a Python object.

    betterproto2 Value uses a oneof kind field; we check which variant is set.
    """
    # betterproto2 sets exactly one of these; check via the 'kind' group
    kind = getattr(value, "kind", None)
    result = _extract_value_from_kind(value, kind)
    if result is not _MISSING_KIND:
        return result
    # Fallback for non-betterproto2 objects
    if hasattr(value, "string_value") and value.string_value:
        return value.string_value
    return None


class LLMHubService(LlmHubRpcBase):
    """gRPC service implementing RuntimeGenerate."""

    async def list_builtin_models(self, request: Any) -> Any:
        try:
            from app.pb.llmhubs.llmhubs import BuiltinModelEntry, ListBuiltinModelsResponse
        except ImportError:
            logger.error("proto stubs not generated yet — run gen.sh core")
            raise

        del request

        response = ListBuiltinModelsResponse(code=0, msg="")
        for entry in sorted(_hub().list_builtin_models(), key=attrgetter("model_key")):
            response.models.append(
                BuiltinModelEntry(
                    model_key=entry.model_key,
                    display_name=entry.display_name,
                    description=entry.description,
                    icon_uri=entry.icon_uri,
                    model_type=entry.model_type,
                    provider_template_type=entry.provider_template_type,
                )
            )
        return response

    async def runtime_generate(self, request: Any) -> Any:
        """Handle RuntimeGenerateRequest → RuntimeGenerateResponse."""
        try:
            from app.pb.llmhubs.llmhubs import RuntimeGenerateResponse
        except ImportError:
            logger.error("proto stubs not generated yet — run gen.sh core")
            raise

        llm_request, resolved_entry = _parse_runtime_request(request)

        result = await generate(llm_request, resolved_entry=resolved_entry)

        # Build proto response
        resp = RuntimeGenerateResponse()
        resp.code = result.code
        resp.msg = result.msg

        from app.pb.llmhubs.llmhubs import (
            RuntimeArtifact,
            RuntimeOutputItem,
            RuntimeTrace,
            RuntimeUsage,
        )

        for o in result.outputs:
            item = RuntimeOutputItem(type=o.type, text=o.text)
            if o.json is not None:
                item.json = Struct.from_dict(o.json)
            if o.call_id:
                item.call_id = o.call_id
            if o.name:
                item.name = o.name
            if o.arguments:
                args = o.arguments
                if isinstance(args, dict):
                    item.arguments = json.dumps(args, ensure_ascii=False)
                else:
                    item.arguments = str(args)
            if o.actions:
                for action in o.actions:
                    item.actions.append(Struct.from_dict(action))
            resp.outputs.append(item)

        for a in result.artifacts:
            resp.artifacts.append(
                RuntimeArtifact(
                    artifact_type=a.artifact_type,
                    mime_type=a.mime_type,
                    filename=a.filename,
                    storage_uri=a.storage_uri,
                    download_url=a.download_url,
                )
            )

        resp.usage = RuntimeUsage(
            prompt_tokens=result.usage.prompt_tokens,
            completion_tokens=result.usage.completion_tokens,
            total_tokens=result.usage.total_tokens,
        )
        resp.trace = RuntimeTrace(
            provider_template_type=result.trace.provider_template_type,
            model=result.trace.model,
            latency_ms=result.trace.latency_ms,
        )
        if result.payload is not None:
            resp.payload = Struct.from_dict(result.payload)

        return resp

    async def runtime_generate_stream(self, request: Any) -> AsyncIterator[Any]:
        """Handle RuntimeGenerateRequest → stream of RuntimeStreamChunk."""
        from app.pb.llmhubs.llmhubs import (
            RuntimeOutputItem,
            RuntimeUsage,
        )
        from app.pb.llmhubs.llmhubs import (
            RuntimeStreamChunk as PbStreamChunk,
        )

        llm_request, resolved_entry = _parse_runtime_request(request)

        try:
            hub = _hub()
            async for chunk in hub.generate_stream(llm_request, resolved_entry=resolved_entry):
                pb_chunk = PbStreamChunk(delta=chunk.delta or "")
                if chunk.finish_reason:
                    pb_chunk.finish_reason = chunk.finish_reason
                if chunk.outputs:
                    for o in chunk.outputs:
                        item = RuntimeOutputItem(type=o.type, text=o.text)
                        if o.call_id:
                            item.call_id = o.call_id
                        if o.name:
                            item.name = o.name
                        if o.arguments:
                            args = o.arguments
                            if isinstance(args, dict):
                                item.arguments = json.dumps(args, ensure_ascii=False)
                            else:
                                item.arguments = str(args)
                        if o.actions:
                            for action in o.actions:
                                item.actions.append(Struct.from_dict(action))
                        pb_chunk.outputs.append(item)
                if chunk.usage:
                    pb_chunk.usage = RuntimeUsage(
                        prompt_tokens=chunk.usage.prompt_tokens,
                        completion_tokens=chunk.usage.completion_tokens,
                        total_tokens=chunk.usage.total_tokens,
                    )
                yield pb_chunk
        except LLMHubRuntimeError as exc:
            yield PbStreamChunk(
                delta=str(exc),
                finish_reason="error",
                code=exc.code or 500,
                msg=str(exc),
            )
        except Exception as exc:
            logger.exception("runtime_generate_stream failed")
            yield PbStreamChunk(
                delta="internal error",
                finish_reason="error",
                code=500,
                msg=str(exc),
            )


def _parse_runtime_request(request: Any) -> tuple[Request, ModelRegistryEntry | None]:
    """Parse a gRPC RuntimeGenerateRequest into Request + optional resolved entry."""
    llm_inputs: list[Input] = []
    for inp in request.inputs or []:
        llm_contents: list[InputContent] = []
        for c in inp.content or []:
            result_val = None
            result_str = getattr(c, "result", "") or ""
            if result_str:
                try:
                    result_val = json.loads(result_str)
                except (ValueError, TypeError):
                    result_val = result_str
            llm_contents.append(
                InputContent(
                    type=c.type,
                    text=c.text,
                    image_base64=c.image_base64,
                    detail=getattr(c, "detail", "") or "",
                    file_url=c.file_url,
                    file_base64=c.file_base64,
                    call_id=getattr(c, "call_id", "") or "",
                    name=getattr(c, "name", "") or "",
                    arguments=getattr(c, "arguments", "") or "",
                    output=_struct_to_dict(getattr(c, "output", None)) or None,
                    image_url=getattr(c, "image_url", "") or "",
                    media_type=getattr(c, "media_type", "") or "",
                    result=result_val,
                    actions=[_struct_to_dict(action) for action in (getattr(c, "actions", None) or [])],
                )
            )
        llm_inputs.append(Input(role=inp.role, content=llm_contents))

    llm_request = Request(
        model=request.model,
        instructions=request.instructions,
        inputs=llm_inputs,
        options=_normalize_integer_request_options(_struct_to_dict(request.options)),
        tools=[_struct_to_dict(t) for t in (request.tools or [])],
        previous_response_id=getattr(request, "previous_response_id", "") or "",
    )
    resolved_entry = _model_definition_to_entry(getattr(request, "model_definition", None))
    return llm_request, resolved_entry


def _model_definition_to_entry(model_definition: Any) -> ModelRegistryEntry | None:
    if model_definition is None:
        return None
    return ModelRegistryEntry(
        model_key=model_definition.model_key,
        display_name=model_definition.display_name or model_definition.model_key,
        model_type=model_definition.model_type,
        provider_template_type=model_definition.provider_template_type,
        status=model_definition.status,
        config=_struct_to_dict(model_definition.config),
        secrets=dict(getattr(model_definition, "secrets", {}) or {}),
    )
