import type { Meta, StoryObj } from "@storybook/react-vite";

import { AssetDetailMetaPanel } from "@/features/projects/components/asset-detail-meta-panel";

// 2026-01-18 16:31 UTC — `formatDateTime` pins UTC so the rendered date is
// deterministic in the Storybook canvas.
const CREATED_AT = Date.UTC(2026, 0, 18, 16, 31);

const meta = {
  title: "Projects/AssetDetailMetaPanel",
  component: AssetDetailMetaPanel,
  // A panel-width column so the stacked Created-time / Operator pair reads at
  // its real 360px shell width.
  decorators: [
    (Story): React.JSX.Element => (
      <div className="w-90 p-6">
        <Story />
      </div>
    ),
  ],
  args: { fileName: "Q4 Market Research.pdf" },
} satisfies Meta<typeof AssetDetailMetaPanel>;

export default meta;
type Story = StoryObj<typeof meta>;

/** All four rows populated — the deliverable case: a Digital Worker authored it
 *  ("Generated by Max") and a human operated the run ("Sarah Bennet"). */
export const FullData: Story = {
  args: {
    createdAt: CREATED_AT,
    dwName: "Max",
    operator: "Sarah Bennet",
  },
};

/** The experience case — the single-row endpoint omits the DW name (falls back
 *  to "Digital worker") and has no operator field yet, so both render the muted
 *  em-dash placeholder while Created time stays real. */
export const MissingDwAndOperator: Story = {
  args: { createdAt: CREATED_AT },
};
